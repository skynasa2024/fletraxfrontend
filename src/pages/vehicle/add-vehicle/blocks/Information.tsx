                        d="M7.78868 1.80365H3.05541C2.62799 1.80365 2.28027 2.16967 2.28027 2.61955V4.83873C2.28027 5.28861 2.62799 5.65463 3.05541 5.65463H7.78868C8.21607 5.65463 8.56378 5.28861 8.56378 4.83873V2.61955C8.56378 2.16967 8.21607 1.80365 7.78868 1.80365ZM3.02246 4.83869V2.61951C3.02246 2.60041 3.03723 2.58486 3.05541 2.58486H4.99527V4.87334H3.05541C3.03727 4.87338 3.02246 4.85783 3.02246 4.83869ZM7.82159 4.83869C7.82159 4.85779 7.80682 4.87334 7.78868 4.87334H5.73749V2.58486H7.78868C7.80682 2.58486 7.82159 2.60041 7.82159 2.61951V4.83869Z"
